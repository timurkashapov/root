/**
 * Координаты.
 */
public class Coordinate {

    private Double x;
    private Double y;

    public Coordinate() {}

    public Double getX() {
        return x;
    }
    public void setX(Double x) {
        this.x = x;
    }
    public Double getY() {
        return y;
    }
    public void setY(Double y) {
        this.y = y;
    }
}
